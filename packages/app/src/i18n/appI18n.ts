export type AppTextKey = string;

function resolveValue(tree: TranslationTree, key: string): string | undefined {
  const parts = key.split('.');
  let cursor: Primitive | TranslationTree | undefined = tree;

  for (const part of parts) {
    if (!cursor || typeof cursor === 'string') return undefined;
    cursor = cursor[part];
  }

  return typeof cursor === 'string' ? cursor : undefined;
}

export function appT(
  key: AppTextKey,
  params?: Readonly<Record<string, string | number>>,
  language: Language = 'zh-CN',
): string {
  const template =
    resolveValue(appText[language], key) ??
    resolveValue(appText['en-US'], key) ??
    key;

  if (!params) return template;
  return template.replace(/\{(\w+)}/g, (match, name: string) => {
    const value = params[name];
    return value === undefined ? match : String(value);
  });
}

export function useAppText(): (key: AppTextKey, params?: Readonly<Record<string, string | number>>) => string {
  const language = useUIStore((state) => state.language);
  return useMemo(
    () => (key: AppTextKey, params?: Readonly<Record<string, string | number>>) => appT(key, params, language),
    [language],
  );
}
