import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import Ajv2020 from 'ajv/dist/2020.js';
import standaloneCode from 'ajv/dist/standalone/index.js';
import unicodeLengthModule from 'ajv/dist/runtime/ucs2length.js';

const packageRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const schemaPath = resolve(packageRoot, 'schema/0.2/story.json');
const outputPath = resolve(packageRoot, 'src/exporter/generated/storySchema02Validator.js');
const declarationPath = resolve(packageRoot, 'src/exporter/generated/storySchema02Validator.d.ts');
const checkOnly = process.argv.includes('--check');
const schema = JSON.parse(await readFile(schemaPath, 'utf8'));
const ajv = new Ajv2020({
  strict: true,
  allErrors: true,
  formats: { 'date-time': true },
  code: { source: true, esm: true },
});
const validate = ajv.compile(schema);
const unicodeLength = unicodeLengthModule.default;
const standalone = standaloneCode(ajv, validate).replace(
  /const (func\d+) = require\("ajv\/dist\/runtime\/ucs2length"\)\.default;/g,
  (_match, functionName) => `const ${functionName} = ${unicodeLength.toString()};`,
);
if (standalone.includes('require(')) {
  throw new Error('Generated browser validator contains an unsupported CommonJS runtime dependency.');
}
const generated = [
  '// Generated by packages/core/scripts/generate-story-schema-validator.mjs.',
  '// Do not edit by hand. The date-time format is checked by exporter/json.ts.',
  standalone,
  '',
].join('\n');
const declaration = [
  '// Generated by packages/core/scripts/generate-story-schema-validator.mjs.',
  'export interface StorySchemaError { readonly instancePath?: string; readonly message?: string; }',
  'export interface StorySchemaValidator {',
  '  (data: unknown): boolean;',
  '  readonly errors?: readonly StorySchemaError[] | null;',
  '}',
  'declare const validate: StorySchemaValidator;',
  'export { validate };',
  'export default validate;',
  '',
].join('\n');

if (checkOnly) {
  const current = await readFile(outputPath, 'utf8').catch(() => '');
  const currentDeclaration = await readFile(declarationPath, 'utf8').catch(() => '');
  if (current !== generated || currentDeclaration !== declaration) {
    console.error('Schema 0.2 standalone validator is stale. Run pnpm generate:schema-validator.');
    process.exitCode = 1;
  } else {
    console.log('Schema 0.2 standalone validator is current.');
  }
} else {
  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, generated, 'utf8');
  await writeFile(declarationPath, declaration, 'utf8');
  console.log(`Generated ${outputPath} and ${declarationPath}`);
}
